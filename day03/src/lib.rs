fn concuss(grid: &Vec<Vec<i8>>, x_step: usize, y_step: usize) -> i32 {
    let wrap = grid[0].len();

    let mut count: i32 = 0;
    let mut x: usize = 0;
    let mut y: usize = 0;
    // println!(
    //     "x, y at start: {},{}; grid[{}][{}]: {}",
    //     x, y, x, y, grid[y][x]
    // );
    while y < grid.len() - 1 {
        x = (x + x_step) % wrap;
        y = y + y_step;
        // if grid[y][x] == 1 {
        //     println!("moved to grid[{}][{}]: {} - Ow!", x, y, grid[y][x]);
        // } else {
        //     println!("moved to grid[{}][{}]: {}", x, y, grid[y][x]);
        // }
        count = count + grid[y][x] as i32;
    }
    // println!("count: {}", count);
    count
}

fn part1(input: Vec<&str>) -> i32 {
    let grid = parse_input(input);
    let x_step = 3;
    let y_step = 1;
    concuss(&grid, x_step, y_step)
}

// convert input to [[0,1,0...]....] where 1 is if input char is #
// in hindsight didn't need to waste time converting chars to 1 or 0 oh well
fn parse_input(input: Vec<&str>) -> Vec<Vec<i8>> {
    let mut grid = Vec::new();
    for line in input {
        let r = line
            .chars()
            .collect::<Vec<_>>()
            .iter()
            .map(|c| if c == &'#' { 1 } else { 0 })
            .collect::<Vec<i8>>();
        grid.push(r);
    }
    grid
}
fn part2(input: Vec<&str>) -> i64 {

    let grid = parse_input(input);

    // Right 1, down 1.
    // Right 3, down 1. (This is the slope you already checked.)
    // Right 5, down 1.
    // Right 7, down 1.
    // Right 1, down 2.

    let slopes = vec![(1,1), (3,1), (5,1), (7,1), (1,2)];

    let mut count:i64 = 1;
    for line in slopes {
        count = count * concuss(&grid, line.0, line.1) as i64
    }

    count
}

